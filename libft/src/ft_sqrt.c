#include "../inc/libft.h"

int		ft_sqrt(int nb)
{
	int i;
	int j;

	i = 1;
	j = 0;
	while (i <= nb / 2)
	{
		j = i * i;
		if (j == nb)
			return (i);
		else
			i++;
	}
	return (0);
}
